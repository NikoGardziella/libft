#include "libft.h"

char	*ft_strncat(char *s1, char const *s2, size_t n)
{
	size_t	d;
	int		i;
	int		c;

	d = 0;
	c = 0;
	i = 0;
	while (s1[i] != '\0')
		i++;
	while (s2[c] != '\0' && d < n)
	{
		s1[i] = s2[c];
		i++;
		c++;
		d++;
	}
	s1[i] = '\0';
	return (s1);
}
